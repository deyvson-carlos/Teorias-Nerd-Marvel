         <p>  <br><b>Mulher-Aranha no MCU</b><br>

        <br>E hoje o famoso e confiável insider Main Middle Man confirmou em seu Twitter que a Sony está trabalhando em um projeto solo da Mulher-Aranha, a Jessica Drew, e que o filme dela se passará no MCU e seria mais um filme feito pela Marvel Studios e Sony Pictures em parceria.<br>
            
        <br>Ainda de acordo com ele, a ideia da Sony de fazer esse novo acordo e incluir a Mulher-Aranha no MCU é trazê-la mais para perto de casa em seu universo. Ele diz que ainda não tem certeza dessas informações, mas ouviu que ao menos o primeiro filme se passaria no MCU.<br></p>

        <br><b>Mulher-Aranha: Revelados os detalhes do filme da Marvel</b><br>

        <br>Já faz um bom tempo que estamos todos cientes que a Sony Pictures produz mais um filme do seu universo de personagens da Marvel, dessa vez com a Mulher-Aranha! O longa foi anunciado como um filme solo de uma super-heroína derivada do Homem-Aranha.

        <br>E que ele seria dirigido pela atriz Olivia Wilde, após o sucesso de seu primeiro filme como diretora (Booksmart). Depois disso, ficamos um tempo sem nenhuma novidade, até agora!<br>

        <br>Segundo o The Illuminerdi, o filme realmente será sobre a Mulher-Aranha, para não deixar nenhuma dúvida. Além disso, o site confirma que a protagonista será a versão original da super-heroína nas HQs: Jessica Drew.<br>

        <br>A Sony está em busca de uma atriz entre 25 e 35 para dar vida à Mulher-Aranha original, e felizmente, a trama também deve se manter fiel à origem da personagem.<br>

        <br>Que nas HQs, mostra uma pequena Jessica Drew ficando extremamente doente e recebendo um soro criado com o sangue de combinadas aranhas. O soro não testado foi dado pelo seu próprio pai, que a mantém em um acelerador genético. Ela acorda anos depois, mas com 17 anos.<br>

        <br>É daí que vem seus poderes e muitas aventuras que incluem uma lavagem cerebral da HYDRA, trabalhando pra SWORD, SHIELD e até pros Vingadores!<br>

        <br>Claro que muitas dessas conexões não devem ser construídas no filme. Mas com certeza espere vê-la ao lado de Venom, Morbius, Kraven e até o próprio Homem-Aranha.<br>

