        <p>  <br><b>Sexteto Sinistro</b><br>

        <br>Existe um projeto de filme solo do Sexteto Sinistro que está engavetado na Sony há anos. O supergrupo de vilões do Homem-Aranha quase apareceu na sequência de O Espetacular Homem-Aranha 2 – que nunca chegou a ser lançada.<br>
            
        <br>Depois que a franquia Homem-Aranha sofreu um reboot, o diretor Drew Goddard foi contratado para dirigir um filme solo do Sexteto Sinistro, que nunca foi desenvolvido. Apesar disso, a Sony ainda espera concluir o projeto em algum momento.<br></p>
