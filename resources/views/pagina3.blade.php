<article id="terceira-NT">
<figure class="fotoss">
<figcaption class="NotíciasX">
<img class="X-Men" src="../imagens/pagina3/xmen99.jpg">
<h5>8 histórias perfeitas dos X-Men para serem adaptadas no MCU </h5>
<h3>Infelizmente, a Fox deixou a franquia X-Men em uma situação bem ruim (especialmente depois de Fênix Negra), então não é de admirar que a Marvel Studios esteja tomando seu tempo quando se trata de reintroduzir esses personagens.</h3>
<h4>POR: Deyvson Carlos</h4>

<p><br><b>⊗ Ultimate X-Men: Retorno do Rei</br></b><span id="Xmen">...</span><span id="terceira"> Nos filmes dos X-Men da Fox, Magneto ficou entre ser um herói e vilão por anos, acabou na prisão e até decidiu se aposentar. No entanto, no arco final da história “O Retorno do Rei”, o Mestre do Magnetismo decidiu dar um basta e se preparou para destruir a Terra, a fim de refazê-la à sua imagem. Há algumas semelhanças com o primeiro filme dos X-Men aqui, mas nesse arco Magneto estava se preparando para um genocídio humano em nível global. Durante este enredo, os poderes do vilão foram amplificados graças a uma máquina construída por Forge. Os X-Men se uniram para derrubar Magneto, e este arco foi tão bom que a série nunca foi capaz de fazer jus a ele. Isso pode ser um culminar épico de anos de histórias.

<br><b>⊗ Gênesis Mortal</br></b>
Esta história revelou que antes de reunir a 2° formação de equipe dos X-Men que incluía heróis como Wolverine, Tempestade e Colossus, o Professor X reuniu outro grupo de mutantes (que incluía o irmão de Ciclope, Vulcano). Todos aparentemente morreram durante sua primeira missão, mas no presente, foi revelado que Gabriel Summers ainda estava vivo…e desesperado por vingança.

A história terminou com Ciclope banindo o Professor Xavier de sua própria escola, deixando-o como o novo líder dos X-Men. Revelações como esta sobre Charles poderiam ajudar o MCU a superar os arcos usados pela Fox.

<br><b>⊗ Massacre dos Mutantes</br></b>
Indiscutivelmente um das histórias mais icônicas dos X-Men de todos os tempos, “Massacre dos Mutantes” introduziu os Morlocks morando nos esgotos e contou com o Sr. Sinistro enviando seus Carrascos para eliminá-los.

Há uma série de personagens queridos que esta adaptação poderia trazer para o Universo Cinematográfico Marvel, incluindo Gambit, e a participação do Sr. Sinistro neste enredo seria uma ótima maneira de trazê-lo para o mundo dos X-Men nos cinemas.

Ver os X-Men levados aos seus limites (muitos deles foram gravemente feridos aqui) seria ótimo, e como Magneto também desempenhou um papel na história, a história criaria muitas bases para o futuro. Em última análise, porém, mostraria que ser um mutante não é só sobre viver em uma mansão de Westchester.

<br><b>⊗ Ninhada</br></b>
Que tal uma invasão alienígena com mutantes!? É aí que a Ninhada entraria. Esse arco das HQs está repleto de lutas e plots épicos, como Wolverine tentando lutar contra a infecção que o transformaria em um dos seres monstruosos.

Adaptar uma história como essa seria algo novo (o que é crucial para os personagens nesta fase), e leva a equipe a uma direção muito inesperada que inclui elementos de ficção científica e horror. Isso o tornaria um conto mais sombrio e maduro, abraçando o lado cósmico da equipe também.

<br><b>⊗ E de Extinção</br></b>
Os X-Men de Grant Morrison apresentaram elementos, plots e relacionamentos que funcionariam muito bem na tela grande. Entre eles está o caso de Scott Summer com Emma Frost, o mistério em torno do mega poderoso Xorn, e uma grande revelação sobre Charles Xavier.

A monstruosa mutação secundária de Fera também seria divertida de explorar, e assim como “E de Extinção” trouxe frescor para esses personagens em um momento em que eles estavam começando a se sentir obsoletos, pegar emprestado ideias desta fase poderia ser uma boa maneira de assumir alguns novos riscos drásticos com esses personagens icônicos.

<br><b>⊗ Vingadores vs. X-Men</br></b>
Embora faça sentido que os X-Men sejam introduzidos em um filme que os coloque contra os Vingadores, faz muito mais sentido que isso seja algo que a Marvel Studios acabe construindo com o tempo. Depois de passar alguns anos familiarizando os fãs com uma nova versão desses personagens mutantes, trazê-los para o radar dos Heróis Mais Poderosos da Terra em uma batalha épica seria alucinante.

Há várias maneiras de fazer isso acontecer, mas um simples mal-entendido – os Vingadores sendo enganados a pensar que os X-Men são a ameaça que o governo e a imprensa afirmam, por exemplo – pode ser o melhor.

<br><b>⊗ Surpreendentes X-Men</br></b>
X-Men: O Confronto Final já usou o arco da cura mutante, e não há razão para trazer isso de volta para o cinema. No entanto, adaptar o arco no alienígena do Grimamundo e usar Ord como um grande vilão funcionaria bem, assim como a decisão da equipe de sair das sombras e finalmente se adequar para que eles possam ser vistos pelo mundo como super-heróis legítimos.

Além disso, poderemos ver outros personagens criados nessa fase, como Perigo(a Sala de Perigo trazida à vida).

<br><b>⊗ Dinastia M</br></b>
Depois dos eventos de WandaVision, Wanda Maximoff acaba de começa a investigar a extensão de suas habilidades como a Feiticeira Escarlate, e seus poderes serão totalmente explorados em Doutor Estranho no Multiverso da Loucura. Espere alguns anos, no entanto, e algum tipo de revelação mutante sobre Wanda poderia muito bem trazê-la para o mundo dos X-Men do MCU.

Dinastia M apresentava muitos personagens dos Vingadores. Wolverine era um dos protagonistas, assim como Magneto e sua família depois que essa realidade alternativa essencialmente os transformou na família real do mundo. Explorar um mundo onde mutantes são a espécie dominante e Magneto governa sobre tudo tem um grande potencial, assim como aquela cena icônica “No More Mutants”.
</span></p>

