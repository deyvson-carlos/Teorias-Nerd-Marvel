<h5>9 SÉRIES DA MARVEL QUE PODEM ESTAR EM PRODUÇÃO PARA O DISNEY+ </h5>
<h3>O estúdio da Marvel pode estar desenvolvendo 9 novas séries para o Disney+! Saiba quais elas poderiam ser.</h3>
<h4>POR: Deyvson Carlos</h4>

<p>O Universo Cinematográfico da Marvel continua se expandindo continuamente, e agora tivemos a notícia de que a Casa das Ideias pode estar desenvolvendo nove séries novas para o catálogo do Disney+. Embora a fonte cite algumas que podem estar em produção, não sabemos exatamente quais serão os planos para esses novos projetos. <span id="pontos">...</span><span id="mais"><br/>

Por isso, fizemos o trabalho de imaginar quais poderiam ser as séries em desenvolvimento no serviço de streaming da Disney, levando em conta alguns dos próximos lançamentos e até mesmo os personagens que acabaram de ser apresentados no MCU. Aqui, você verá 9 séries que podem estar sendo desenvolvidas pela Marvel!<br/>

<b>Motoqueiro Fantasma</b>

<br/>No rumor originalmente trazido pelo insider Charles Murphy, um dos três nomes que aparecia forte era o Motoqueiro Fantasma, um anti-herói bem querido pelos fãs da Marvel e que já ganhou algumas adaptações no cinema antes da incorporação ao Universo Cinematográfico da Marvel. E depois, tivemos outra versão do personagem em Agentes da S.H.I.E.L.D..

<br/>Levando em conta que a mitologia do personagem está diretamente atrelada ao ocultismo e satanismo, é um pouco curioso imaginar como ele pode ser adaptado em uma série do Disney+, mas vale lembrar que a plataforma também está produzindo um material focado no Cavaleiro da Lua, então nos resta esperar para saber se isso é real e se a série fará justiça ao herói.<br/>

<b>Nova</b><br/>
<br/>
Outro nome presente no rumor de Charles Murphy, o Nova é uma figura que supostamente está orbitando o Universo Cinematográfico da Marvel há anos - e o melhor de tudo é que há duas versões possíveis que podem estrelar franquias bem interessantes. O primeiro deles é Richard Rider, o Nova "original", um herói mais adulto e sério.
<br/>
O segundo é Sam Alexander, um jovem que ganha poderes cósmicos quando descobre o capacete que pertencia ao seu pai. Embora os dois tenham histórias incríveis, a maior aposta é Sam, já que ele faz parte dos Campeões - uma equipe de heróis juvenis que conta inclusive com a Ms. Marvel (e por isso, pode ganhar adaptação no futuro).
<br/>

<b>Guerreiros Secretos</b><br/>

Fechando a trindade dos nomes citados por Charles Murphy, nós temos também os Guerreiros Secretos, uma equipe bem interessante e que já teve várias versões ao longo dos anos. A mais conhecida é liderada por Nick Fury e conta com vários heróis obscuros, como Phobos, Dobra, Infernal e o Druida, além de Tremor.
<br/>
Essa equipe recentemente ganhou uma reformulação nos quadrinhos, sendo liderada por Tremor e contando com a participação de alguns heróis dos Inumanos, como Dante e a própria Ms. Marvel. Qualquer uma das versões seria incrível de ver no MCU, especialmente se isso significar que a Marvel vai reaproveitar personagens de Agentes da S.H.I.E.L.D....
<br/>
<b>Valquíria</b><br/>

Agora, passando para o campo da especulação mais selvagem, temos vários personagens que certamente podem ganhar suas próprias séries solo no Universo Cinematográfico da Marvel, a começar pela Valquíria - uma heroína que nos acompanha desde Thor: Ragnarok e que hoje tem o papel fundamental de Rei de Asgard, assumindo o Reino Dourado para si.
<br/>
Se pensarmos que Loki afastou o vilão da mitologia nórdica e Thor: Love and Thunder deve levar o Deus do Trovão a um núcleo mais espacial, faria total sentido pensar que uma série focada na Valquíria pode nos levar de volta ao reino e explorar mais da mitologia asgardiana. Ela também poderia liderar sua própria equipe (como nas HQs), as Destemidas Defensoras.
<br/>
<b>Viúva Negra</b><br/>

Agora, precisamos pensar um pouco na mais nova adição ao MCU, como visto no filme solo da Viúva Negra. Nele, conhecemos Yelena Belova, a irmã de Natasha, que permanece viva e deve substituí-la como a Viúva Negra "principal" da Marvel. A inclusão da heroína foi muito bem-vinda, e já sabemos que ela terá uma participação na série dos Gaviões Arqueiros.
<br/>
Contudo, a Marvel e a Disney não precisam repetir os mesmos erros que cometeram com Natasha e podem dar a Yelena sua própria série, onde nós veríamos mais tramas de espionagem e ação, enquanto somos revisitados por outras figuras populares como o Guardião Vermelho ou até mesmo o Soldado Invernal.
<br/>
<b>Thunderbolts</b><br/>

Mas caso isso não aconteça, há outro lugar onde ainda poderíamos ver a nova Viúva Negra em ação. Em Falcão e o Soldado Invernal, nós fomos apresentados a Valentina Allegra de Fontaine, uma figura peculiar que, ao que tudo indica, está montando sua própria equipe, tendo recrutado tanto o Agente Americano quanto Yelena Belova.
<br/>
Muitos acreditam que ela está, na verdade, formulando sua própria versão dos Thunderbolts - um grupo composto por anti-heróis e vilões da Casa das Ideias. Se isso for real, faria todo o sentido trazer esses personagens para uma série do Disney+, antes de dar a eles um papel maior nos cinemas - vale lembrar que foi a partir deles que surgiram os Vingadores Sombrios...
<br/>
<b>Jovens Vingadores</b><br/>

Podemos dar como quase uma certeza que a Marvel Studios está pensando em fazer algo para os Jovens Vingadores em um futuro não tão distante. Basta pensar que, nos últimos anos, tivemos a inclusão de diversas figuras que fazem parte da equipe, como os filhos da Feiticeira Escarlate, o Kid Loki e a Estatura - sem contar nos outros que já foram anunciados.
<br/>
Como a equipe é juvenil e suas histórias sempre foram mais voltadas para um público adolescente e jovem adulto, faz todo sentido que eles ganhem uma produção no Disney+, já que esse é o público alvo da plataforma. Há até quem ache que a equipe vai incorporar traços dos Campeões nas HQs, talvez contando com a presença da Ms. Marvel e da Coração de Ferro.
<br/>
<b>Doutor Destino</b><br/>

Porém, se podemos ir ainda mais longe nas teorias, um rumor que está cada vez mais presente na internet é de que a Disney estaria bem interessada em criar uma mitologia própria para o Doutor Destino antes de colocá-lo para enfrentar o Quarteto Fantástico no Universo Cinematográfico da Marvel. E que forma melhor de fazer isso que não uma série solo?
<br/>
Victor Von Doom sempre foi um personagem castigado nas adaptações anteriores, sempre descaracterizado, e talvez uma série possa apresentar a versão mais fiel do vilão, desde sua origem trágica ao momento em que se tornou um dos maiores tiranos do mundo, governando a Latvéria com punhos de aço. Tudo isso encaixaria perfeitamente numa série do Disney+.
<br/>
<b>X-Men (ou algo envolvendo os mutantes)</b><br/>

Por fim, permita-nos voar ainda mais alto. Sabemos que, depois que a Casa das Ideias colocar sua adaptação do Quarteto Fantástico em ação, ela vai precisar focar em outra propriedade que pertencia à Fox e que muitos fãs estão doidos para ver nos cinemas: os X-Men, a equipe composta pelos mutantes e liderada pelo Professor X.
<br/>
Se a Disney estiver realmente disposta a fazer algo diferente do que já nos foi apresentado no passado, faria sentido desenvolver uma série para o grupo - mesmo que ainda houvesse uma franquia cinematográfica para arrecadar rios de dinheiro nos cinemas. Ou talvez, pode ser uma produção mais contida no Disney+, dando espaço para mutantes conhecidos como Wolverine ou Tempestade.</span></p>

